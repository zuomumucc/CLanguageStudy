// 游戏的整个算法实现
void game()
{
	char ret = 0;
	// 数组 - 存放走出的棋盘信息
	char board[ROW][COL] = {0};

	// 初始化棋盘
	InitBoard(board, ROW, COL);

	// 打印棋盘
	DisplayBoard(board, ROW, COL);

	// 下棋
	while (1)
	{
		// 玩家下棋
		PlayerMove(board, ROW, COL);

		// 打印棋盘
		DisplayBoard(board, ROW, COL);
		// 判断玩家是否赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}

		// 电脑下棋
		ComputerMove(board, ROW, COL);

		// 打印棋盘
		DisplayBoard(board, ROW, COL);
		// 判断玩家是否赢
		ret = IsWin(board, ROW, COL);
		if (ret != 'C')
		{
			break;
		}
	}

	if (ret == '*')
	{
		printf("玩家赢!!!\n\n");
	}
	else if (ret == '#')
	{
		printf("电脑赢!!!\n\n");
	}
	else
	{
		printf("平局!!!\n\n");
	}
}